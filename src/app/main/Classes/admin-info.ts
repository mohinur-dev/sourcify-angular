export class AdminInfo {
    id?:any;
    name?:any;
    role?:any;
    email?:any;
    username?:any;
    picture?:any;

    constructor(id?:any, name?:any, role?:any, email?:any, username?:any, picture?:any) {
        this.id = id;
        this.name = name;
        this.role = role;
        this.email = email;
        this.username = username;
        this.picture = picture;
    }
}
